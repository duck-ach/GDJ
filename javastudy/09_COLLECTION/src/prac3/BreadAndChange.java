package prac3;

public class BreadAndChange {
// 빵 그리고 잔돈
	private int bread;
	private int change;
	
	public BreadAndChange() {
	
	}

	public BreadAndChange(int bread, int change) {
		super();
		this.bread = bread;
		this.change = change;
	}


	public int getBread() {
		return bread;
	}

	public void setBread(int bread) {
		this.bread = bread;
	}

	public int getChange() {
		return change;
	}

	public void setChange(int change) {
		this.change = change;
	}
	
}
